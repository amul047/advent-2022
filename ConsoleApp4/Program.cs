const string rock = "A";
const string paper = "B";
const string scissor = "C";
const string loose = "X";
const string draw = "Y";
const string win = "Z";

var totalScore = 0;

foreach (var psrRound in psrRounds)
{
    var hands = psrRound.Split(" ");
    var opponentHand = hands[0];
    var result = hands[1];

    totalScore += GetRoundScore(result);
    var myRequiredShape = GetRequiredShape(result, opponentHand);
    totalScore += GetShapeScore(myRequiredShape);
}

