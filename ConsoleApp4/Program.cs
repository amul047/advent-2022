string GetRequiredShape(string result, string opponentHand)
{
    if (result == draw) return opponentHand;

    if (result == win)
    {
        if (opponentHand == rock) return paper;
        if (opponentHand == paper) return scissor;
        if (opponentHand == scissor) return rock;
    }

    if (result == loose)
    {
        if (opponentHand == rock) return scissor;
        if (opponentHand == paper) return rock;
        if (opponentHand == scissor) return paper;
    }

    throw new NotImplementedException();
}

int GetRoundScore(string result)
{
    if (result == win) return 6;
    if (result == draw) return 3;

    return 0;
}


